package uk.ac.ebi.fgpt.conan.core.context.locality;

public class ConnectionDetails {

    private String host;
    private int port;
    private String username;
    private String password;

    public ConnectionDetails(String host, int port, String username,
                             String password) {
        this.host = host;
        this.port = port;
        this.username = username;
        this.password = password;
    }

    public ConnectionDetails(ConnectionDetails copy) {
        this(copy.getHost(), copy.getPort(), copy.getUsername(), copy.getPassword());
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    @Override
    public String toString() {
        return this.username + "@" + this.host + ":" + this.port;
    }


}
